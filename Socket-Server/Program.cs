using System;
using System.Text;
using System.Net;
using System.Net.Sockets;
using System.IO;

namespace Socket_Server
{
    public class Program
    {
        public static void Main(string[] args)
        {
            // Устанавливаем для сокета локальную конечную точку
            IPHostEntry ipHost = Dns.GetHostEntry("127.0.0.1");
            IPAddress ipAddr = ipHost.AddressList[0];
            IPEndPoint ipEndPoint = new IPEndPoint(ipAddr, 5000);
                // Создаем сокет Tcp/Ip
            Socket sListener = new Socket(ipAddr.AddressFamily, SocketType.Stream, ProtocolType.Tcp);
            // Назначаем сокет локальной конечной точке и слушаем входящие сокеты
            try
            {
                sListener.Bind(ipEndPoint);
                sListener.Listen(10);

                // Начинаем слушать соединения
                while (true)
                {
                    //Console.WriteLine("Ожидаем соединение через порт {0}", ipEndPoint);

                    // Программа приостанавливается, ожидая входящее соединение
                    Socket handler = sListener.Accept();

                    // Мы дождались клиента, пытающегося с нами соединиться

                    byte[] bytes = new byte[1024];
                    int bytesRec = handler.Receive(bytes);
                    // Приходит на сервер
                    var data = Encoding.UTF8.GetString(bytes, 0, bytesRec);

                    // Показываем данные на консоли
                    //Console.Write("Полученный текст: " + data + "\n\n");
                    string reply = "";
                    if(data == "Пидорас")
                    {
                        reply = "Пошел нахуй, гандон";
                    }
                    else
                    {
                        // Отправляем ответ клиенту\
                        reply = "Спасибо за запрос в " + data.Length.ToString()
                                + " символов";
                    }
                    
                    byte[] msg = Encoding.UTF8.GetBytes(reply);
                    handler.Send(msg);

                    if (data.IndexOf("<TheEnd>") > -1)
                    {
                        //Console.WriteLine("Сервер завершил соединение с клиентом.");
                        break;
                    }

                    handler.Shutdown(SocketShutdown.Both);
                    handler.Close();
                }
            }
            catch (Exception ex)
            {
                using (StreamWriter f = new StreamWriter("test.txt"))
                {
                    string str = ex.ToString();
                    f.Write(str);
                }
            }
            finally
            {
                //Console.ReadLine();
            }
        }
    }
}
